package toolgateway

import (
	"context"
	"encoding/json"
	"errors"
	"fmt"
	"strings"
	"unicode/utf8"
)

const (
	ScriptProcessSchema             = "script_process.v1"
	ScriptProcessExecutionDisabled  = "disabled"
	ScriptProcessBackendSandbox     = "sandbox"
	ScriptProcessBackendLocal       = "local"
	MaxScriptProcessArguments       = 128
	MaxScriptProcessExecutableBytes = 4096
	MaxScriptProcessArgumentBytes   = 8192
)

type ScriptProcessProposal struct {
	Executable       string
	Arguments        []string
	WorkingDirectory string
	RequestedBackend string
}

type scriptProcessEnvelope struct {
	Schema           string   `json:"schema"`
	Executable       string   `json:"executable"`
	Arguments        []string `json:"arguments"`
	WorkingDirectory string   `json:"working_directory"`
	RequestedBackend string   `json:"requested_backend"`
	ExecutionMode    string   `json:"execution_mode"`
}

func NormalizeScriptProcessProposal(proposal ScriptProcessProposal) (ScriptProcessProposal, error) {
	proposal.Executable = strings.TrimSpace(proposal.Executable)
	proposal.WorkingDirectory = strings.TrimSpace(proposal.WorkingDirectory)
	proposal.RequestedBackend = strings.ToLower(strings.TrimSpace(proposal.RequestedBackend))
	if proposal.WorkingDirectory == "" {
		proposal.WorkingDirectory = "."
	}
	if proposal.RequestedBackend == "" {
		proposal.RequestedBackend = ScriptProcessBackendSandbox
	}
	if proposal.Executable == "" {
		return ScriptProcessProposal{}, errors.New("script process executable is required")
	}
	if !utf8.ValidString(proposal.Executable) || strings.ContainsRune(proposal.Executable, 0) ||
		len([]byte(proposal.Executable)) > MaxScriptProcessExecutableBytes {
		return ScriptProcessProposal{}, fmt.Errorf("script process executable must be valid UTF-8 without NUL and at most %d bytes", MaxScriptProcessExecutableBytes)
	}
	if proposal.WorkingDirectory != "." {
		return ScriptProcessProposal{}, errors.New("script process working directory must be the workspace root")
	}
	if proposal.RequestedBackend != ScriptProcessBackendSandbox && proposal.RequestedBackend != ScriptProcessBackendLocal {
		return ScriptProcessProposal{}, fmt.Errorf("unsupported script process backend %q", proposal.RequestedBackend)
	}
	if len(proposal.Arguments) > MaxScriptProcessArguments {
		return ScriptProcessProposal{}, fmt.Errorf("script process arguments exceed %d items", MaxScriptProcessArguments)
	}
	arguments := make([]string, len(proposal.Arguments))
	for index, argument := range proposal.Arguments {
		if !utf8.ValidString(argument) || strings.ContainsRune(argument, 0) || len([]byte(argument)) > MaxScriptProcessArgumentBytes {
			return ScriptProcessProposal{}, fmt.Errorf("script process argument %d must be valid UTF-8 without NUL and at most %d bytes", index, MaxScriptProcessArgumentBytes)
		}
		arguments[index] = argument
	}
	proposal.Arguments = arguments
	return proposal, nil
}

func EncodeScriptProcessProposal(proposal ScriptProcessProposal) (string, error) {
	normalized, err := NormalizeScriptProcessProposal(proposal)
	if err != nil {
		return "", err
	}
	payload, err := json.Marshal(scriptProcessEnvelope{
		Schema: ScriptProcessSchema, Executable: normalized.Executable, Arguments: normalized.Arguments,
		WorkingDirectory: normalized.WorkingDirectory, RequestedBackend: normalized.RequestedBackend,
		ExecutionMode: ScriptProcessExecutionDisabled,
	})
	if err != nil {
		return "", err
	}
	if len(payload) > MaxCommandBytes {
		return "", fmt.Errorf("script process proposal exceeds %d bytes", MaxCommandBytes)
	}
	return string(payload), nil
}

func (g *Gateway) ProposeScriptProcess(ctx context.Context, call ToolCall, proposal ScriptProcessProposal) (Outcome, error) {
	if call.Name != "" && call.Name != ShellTool {
		return Outcome{}, fmt.Errorf("script process cannot use tool %q", call.Name)
	}
	if len(call.Arguments) != 0 {
		return Outcome{}, errors.New("script process call arguments are generated by the gateway")
	}
	payload, err := EncodeScriptProcessProposal(proposal)
	if err != nil {
		return Outcome{}, err
	}
	call.Name = ShellTool
	call.Arguments = map[string]string{"command": payload}
	if strings.TrimSpace(call.RequestedBy) == "" {
		call.RequestedBy = "script_process"
	}
	return g.Invoke(ctx, call)
}
